#include "main.h"
/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub two numbers
 * @a: first number
 * @b: second number
 * Return: sub of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 * Return: div of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod od  two numbers
 * @a: first number
 * @b: second number
 * Return: mod of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}

